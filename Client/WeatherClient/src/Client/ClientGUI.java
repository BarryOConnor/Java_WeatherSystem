    private void cboConnectedSensorsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboConnectedSensorsActionPerformed
        parent.requestData((String)cboConnectedSensors.getSelectedItem());
        int rowCount = sensorDataModel.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            sensorDataModel.removeRow(i);
        }
        lblGPS.setText("");
        pbDiskSpace.setValue(0);
        pbBatteryLevel.setValue(0);  
    }//GEN-LAST:event_cboConnectedSensorsActionPerformed

