    public void processSensorData(String serverMessage){
        String[] serverCommand = serverMessage.split("\\|");
        if (serverCommand != null && serverCommand.length > 0) {
            String GPS = serverCommand[2];
            int batteryLevel = (int)Double.parseDouble(serverCommand[3]);
            int freeSpace = (int)Double.parseDouble(serverCommand[4]);
            double humidity = Double.parseDouble(serverCommand[5]);
            double temperature = Double.parseDouble(serverCommand[6]);
            double airPressure = Double.parseDouble(serverCommand[7]);
            double precipitation = Double.parseDouble(serverCommand[8]);
            double windSpeed = Double.parseDouble(serverCommand[9]);
            String windDirection = serverCommand[10];
            double snowFall = Double.parseDouble(serverCommand[11]);

            sensorDataModel.addRow(new String[] {Double.toString(temperature), Double.toString(airPressure), Double.toString(humidity), Double.toString(precipitation), Double.toString(windSpeed), windDirection, Double.toString(snowFall)});
            lblGPS.setText(GPS);

            pbTemperature.setValue((int) (temperature + 5 * 2.5));
            pbHumidity.setValue((int) (humidity));
            pbPrecipitation.setValue((int) (precipitation * 2));
            pbAirPressure.setValue((int) (airPressure - 950));
            pbWindSpeed.setValue((int) (windSpeed * 2.5));
            pbSnowFall.setValue((int) (snowFall * 20));
            
            pbDiskSpace.setValue(freeSpace);
            if(freeSpace <= 20) {
                pbDiskSpace.setForeground(Color.red);
            } else {
                pbDiskSpace.setForeground(Color.green);
            }
            
            pbBatteryLevel.setValue(batteryLevel); 
            if(batteryLevel <= 20) {
                pbBatteryLevel.setForeground(Color.red);
            } else {
                pbBatteryLevel.setForeground(Color.green);
            }
            
                      
        }
    }
    
